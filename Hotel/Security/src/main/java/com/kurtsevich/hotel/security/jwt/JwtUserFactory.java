package com.kurtsevich.hotel.security.jwt;

import com.kurtsevich.hotel.model.security.Role;
import com.kurtsevich.hotel.model.security.Status;
import com.kurtsevich.hotel.model.security.User;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;

import java.util.ArrayList;
import java.util.List;
import java.util.stream.Collectors;

public final class JwtUserFactory {

    public static JwtUser create(User user) {
        return new JwtUser(
                user.getId(),
                user.getUsername(),
                user.getFirstName(),
                user.getLastName(),
                user.getEmail(),
                user.getPassword(),
                user.getUpdated(),
                user.getActiveStatus().equals(Status.ACTIVE),
                mapToGrantedAuthorities(new ArrayList<>( user.getRoles()))
        );
    }

    private static List<GrantedAuthority> mapToGrantedAuthorities(List<Role> userRoles) {
        return userRoles.stream()
                .map(role -> new SimpleGrantedAuthority(role.getName()))
                .collect(Collectors.toList());
    }
}
